process.argv // Um array de linha de comando
process.arch // A arquitetura da CPU: "x64", por examplo.
process.cwd() // retorna o diretório corrente de trabalho.
process.chdir() // Define o diretório corrente de trabalho.
process.cpuUsage() // Reporta o uso do CPU.
process.env // Um objeto de variáveis de ambiente.
process.execPath // O caminho absoluto do sistema de arquivo até o executável do node.
process.exit() // Finaliza o programa.
process.exitCode // Um código do tipo inteiro a ser reportado quando o programa encerra.
process.getuid() // Retorna o id de usuário Unix do usuário atual.
process.hrtime.bigint() // Retorna um "high-resolution" nanosecond timestamp.
process.kill() // Envia um sinal para outro processo.